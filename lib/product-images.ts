// Function to get image for a specific product
export function getProductImage(productTitle: string, category?: string): string {
  // First check for specific product mapping
  if (specificProductImages[productTitle]) {
    return specificProductImages[productTitle]
  }

  // Check for partial matches in product title
  for (const [key, image] of Object.entries(specificProductImages)) {
    if (productTitle.toLowerCase().includes(key.toLowerCase()) || 
        key.toLowerCase().includes(productTitle.toLowerCase())) {
      return image
    }
  }

  // Fall back to category-based selection
  if (category) {
    return getRandomImageForCategory(category)
  }

  // Final fallback
  return productImages.placeholder
}
